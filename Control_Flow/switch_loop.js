const month=5
switch (month) {
    case 1:
        console.log("It's January");        
        break;
    case 2:
        console.log("It's February");        
        break;
    case 3:
        console.log("It's March");        
        break;
    case 4:
        console.log("It's April");        
        break;
    case 5:
        console.log("It's May");        
        break;

    default:
        console.log("It's August"); 
        break;
}